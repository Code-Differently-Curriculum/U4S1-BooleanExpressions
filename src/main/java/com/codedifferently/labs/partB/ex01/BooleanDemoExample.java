public class BooleanDemoExample {
    public static void main(String args[]) {

        boolean passed, largeVenue, grade;

        passed = true;
        largeVenue = false;
        grade = passed;

        System.out.println(passed);
        System.out.println(largeVenue);
        System.out.println(grade);
    }
}
